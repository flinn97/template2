class MainContent extends Component{
  constructor(props) {
    super(props);
  }

  componentDidMount(){
    let app = this.props.app
    let state = app.state;
    let dispatch=app.dispatch;
    if(!state.currentStudent){
      let student = state.componentList.getComponent("student");
      dispatch({currentStudent:student,  })

    }
    if(!state.currentRoutine){
      let routine = state.componentList.getComponent("routine");
      dispatch({ currentRoutine:routine })

    }
  }
  render(){
    let app = this.props.app;
    let dispatch = app.dispatch;
    let state = app.state;
    let componentList = state.componentList;
    let styles =state.styles;
    

    return(
    <div style={{marginLeft:"100px"}}>
    {state.currentRoutine&&(
      <MapComponent name = "assignedCard" filter={{search:state.currentRoutine.getJson()._id, attribute:"routineID"}} app={app} theme = "gridMap" cells={[{custom:ViewCards, props:{app:app}}]} />
      )}
    </div>
    )
  }
}

